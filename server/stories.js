export const stories = (() => {
  const list = []

  for (let i = 1; i <= 10; i++) {
    const real = realStories[String(i)]
    list.push(real || makePlaceholder(i, 'Simple'))
  }
  for (let i = 11; i <= 30; i++) {
    const real = realStories[String(i)]
    list.push(real || makePlaceholder(i, 'Medium'))
  }
  for (let i = 31; i <= 60; i++) {
    const real = realStories[String(i)]
    list.push(real || makePlaceholder(i, 'Hard'))
  }

  return list
})()

export function getStoryById(id) {
  return stories.find((s) => s.id === String(id))
}

