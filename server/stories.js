    id: String(id),
    title: `${label} #${id}`,
    difficulty,
    surface:
      '这是一则尚待设计的海龟汤案件，占位用关卡（后端）。与前端 stories.ts 中的占位故事对应，用于保证 DeepSeek 提示正常工作。',
